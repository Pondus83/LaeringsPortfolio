                <h2>Forretningsmodel</h2>

                <h3>Beskrivelse</h3>
                <p>En forretningsmodel kortlægger hvordan der bliver skabt værdi. Kunden får/køber et produkt som tilføjer denne værdi. Virksomheden generer overskud gennem salg eller anden form for indtægt og genererer derved værdi for ejerne. For at finde ud af om et produkt har en fornuftig forretningsstrategi, vil det være givtigt at lave en forretningsmodel. Herved kortlægges hvad værdien er, hvem kunden er og hvordan værdien skabes i virksomheden.</p>

                <h3>Business Model Canvas</h3>
                <p>BMC er en forretningsmodel som hjælper virksomheder med at analysere hvor deres stærke sider er og hvor de har muligheder for forbedringer. Dette gøres ved hjælp af et lærred og 9 byggesten.</p>
                <p>Byggestenene kan inddeles i fire grupper.</p>
                <p>Værditilbud byggestenen er en gruppe for sig som beskriver <strong>hvad</strong> der giver værdi for kunden og derved også hvad det skal produceres af virksomheden.</p>
                <p>Kunden har tre byggesten som beskriver hvem</strong> kunden er. Kundesegmenter beskriver segmentet af personer som virksomheden rammer. Kunderelationer beskriver hvordan kunderne bliver opmærksomme på værdien og hvordan de bliver fastholdt i opmærksomheden. Kanaler beskriver hvordan værdien bliver leveret til kunden.</p>
                <p>Virksomheden har også tre byggesten som beskriver <strong>hvordan</strong> værdien bliver produceret. Nøglepartnere beskriver hvilke partnere der skal til for at virksomheden kan levere værditilbuddet. Denne kan deles op i aktive nøglepartnere som skal gøre noget aktivt for at være partner og passive som ikke gør noget aktivt for virksomheden, men som stadig har indflydelse på dennes forretningsmodel. Nøgleaktiviteter beskriver hvilken aktiviteter der skal til for at producere værditilbuddet. Nøgleressourcer beskriver hvilke ressourcer virksomheden skal bruge for at producere værditilbuddet.</p>
                <p>De sidste to byggesten beskriver <strong>pengestrømmen</strong>, altså hvilke omkostninger der er forbundet med at lave forretningen og hvor indtægterne kommer fra.</p>
