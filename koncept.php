                <h3>Styletiles</h3>
                <p>Styletiles kan også oversættes til fliser med den ønskede stil. Her er en samling af de font man ønsker skriften skal skives i, en farvekombination og hvordan link skal se ud. Det er hurtigt at lave flere styletiles og vise kunden som så kan vælge. Når kunden har valgt en style, kan man implementere den på hele hjemmesiden.</p>

                <h3>Wireframe</h3>
                <p>Wireframe går ud på at tegne en hurtig skitse over hvordan man tænker en hjemmeside, i grove træk, kommer til at se ud. Når man har denne wireframe/skitse, kan man danne sig en ide om ens design kommer til at virke, om man skal ændre lidt i designet eller man skal tænke designet helt forfra.<br>Wireframes er en god måde at få noget konkret, omhandlende hvordan en hjemmeside skal se ud, uden at skulle bruge lang tid på at kode den.</p>
                <p>Wireframes er super gode at bruge til brugertest, da brugeren har noget konkret at se og "røre" ved. Dette bevirker at brugeren, som jo er ekspert på hvad denne hjemmeside skal kunne, hurtigt kan sige om designet virker hensigtsmæssigt eller komme med konkrete forbedrings forslag tilbage til udvikleren.</p>

                <h3>Versionsstyring</h3>
                <p>Versionsstyring går ud på at styre hvilken version, af ens kode, man arbejder i. Det er en nødvendighed, hvis man arbejder flere personer på et projekt, at der er en form for styring af koden, ellers kan der hurtigt kommer til at ske sletninger, overskrivninger eller dobbeltkode</p>
