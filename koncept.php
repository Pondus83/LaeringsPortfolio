                <h3>MoodBord</h3>
                <p>Et moodboard kan direkte oversættes til et humør-bræt, og det er lige hvad det er. Et bræt med det humør som man ønsker at udtrykke med ens hjemmeside. Her kan være forskellige farver, billeder, mønstre og andet man finder inspirerende. Nu gælder det om at have dette bræt i nærheden mens man udvikler, for derigennem at få inspiration til hvordan hjemmesiden skal se ud.</p>
