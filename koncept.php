                <h2>Design Thinking</h2>

                <h3>Beskrivelse</h3>
                <p>Design Thinking handler om at forstå problemet. For virkelig at forstå et problem kræver det at vi sætter og ind i det. Derfor må vi ikke reagere, ved at starte med at tænke i løsninger. Dette er dog stadig en god taktik hvis man står på savannen og en løve kommer løbende. Løsning = løb og se om der ikke er noget man kan bruge. Alle de løsninger vi finder i dette tilfælde er bygget på den begrænsede viden vi har i forvejen.<br>Ved Design Thinking vil vi tvinge hjernen til at tænke i spørgsmål der kan få os til at komme frem med andre ideer.</p>

                <h3>Den kreative kurve</h3>
                <p>Når man skal være kreativ, er det vigtigt at man ikke udelukker ideer, da disse måske kan være vigtige sidenhen.</p>
                <a href="fotos/den_kreative_kurve.jpg" target="_blank">
                    <img src="fotos/den_kreative_kurve.jpg" alt="Ilustration af en divergent og convergent proces">
                    <blockquote>En kreativ proces kræver divergent og convergent tænkning. Kilde: Undervisning d. 24. Oktober</blockquote>
                </a>

                <h3>Faser</h3>
                <p>Når vi arbejder med Design Thinking er der 6 faser vi skal igennem.</p>
                <ul>
                    <li>Empathize - Her sætter vi os i andres sted for at forstå, altså det væsentlige i denne metode. Øvelsen i at pakker sig selv væk, og se hvad der er  på spil for brugerne gennem interviews og observationer. Vi er i en divergent fase hvor man ikke må sige nej til selv de vildeste ideer.</li>
                    <li>Define - Her definere vi problemet som der skal findes en løsning på ved at undersøge hvad det faktisk er vi har fundet ud af. Vi holder os i den divergente fase og er stadig åbne og nysgerrige.</li>
                    <li>Ideate - Ideudvikling gennem brainstorming eller asosiationer. Der er et hav af måder at ideudvikle. Hvis vi har fundet 100 ideer, skal disse insnævres til 5 vi vil går videre med.</li>
                    <li>Prototype - Det er vigtigt at prøve sine ideer af. Der bliver lavet wireframe eller andet som hurtigt kan stykkes sammen, det behøves ikke være flot, det er funktionaliteten vi er ude efter at teste.</li>
                    <li>Test - De 5 prototyper bliver testet af brugerne. Og der bliver udvalgt én.</li>
                    <li>Implement - Her phiches ideen til en invester. Hvis denne godkender ideeen sættes produktions i gang og ideen overleveres til kunden.</li>
                </ul>
                <a href="fotos/DesignThinking.jpg" target="_blank">
                    <img src="fotos/DesignThinking.jpg" alt="Ilustration af faserne i design thinking">
                    <blockquote>Ilustration af de 6 faser i Design Thinking</blockquote>
                </a>
                <p>Hele Design Thinking tankegangen er regressiv. Dette er illustreret på det overstående billede. Det gælder ikke om at komme hurtigt og billigt frem til den første løsning, det gælder om at komme frem til den rigtige løsning. Derfor er der hele tiden muligheder for at springe tilbage i processen, hvis man har fået nye oplysninger der ændre resultatets brugbarhed.</p>
                <p>Et godt citat, jeg også har lagt mærke til i spillet civilization:</p>
                <p><i>Perfection is achieved, not when there is nothing more to add, but when there is nothing left to take away.</i></p>
                <p><small>Antoine de Saint-Exupery</small></p>
                <p>Det beskiver hvordan det ikke er nogen kunst at gøre noget mere indviklet. Det svære kommer i at gøre noget så enkelt at alle kan forstå det.</p>
