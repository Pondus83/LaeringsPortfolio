                <h2>Typografi</h2>

                <h3>Beskrivelse</h3>
                <p>Typografi dækker over hvordan vores skrifttegn er opbygget. Før jeg havde denne lektion, havde jeg aldrig skænket det en tanke, at der sidder mennesker derude og designer hvordan samtlige bogstaver, store/små/kursive, skal se ud. Så jeg blev meget overrasket over hvormeget nogle folk gik op i hvilken skriftype der bliver benyttet.</p>

                <h3>Fontfamilie</h3>
                <p>Tegn der er designet til at passe sammen kommer i den sammen fontfamilie. Når man designer en ny fontfamilie, skal man tage højde til hvordan tegnet skal se ud. Man kan ikke opfinde nye bogstaver, men man kan tegne dem på nye måder. Man skal også tage stilling til hvor langt der skal være mellem hvert tegn og hvor høje de forskellige tegn skal være. Man kan også tegne dem med serif eller sans serif, altså med eller uden fødder på hvert tegn.</p>

                <h3>Læsbarhed</h3>
                <p>Hvis du tænker over skriften, så er der sket noget forkert. En god typografi er som luft, du vil opdage den hvis den mangler, for så er der intet at læse, men du tænker ikke over den når den virker. Jeg har faktisk kun erfaring med det sidste.</p>
