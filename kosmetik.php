            <section id="_tre">
                <h2>Fotografering</h2>

                <h3>Beskrivelse</h3>
                <p>Fotografering er læren om hvordan man tegner med lys. Vi har fået stillet nogle prosumer-kameraer til rådighed, jeg har selv et lidt billigere, og så har vi eksperimenteret med at instille og tage billerder.</p>

                <h3>Lukketid</h3>
                <p>En af de ting som man kan indstille på et spejlreflekskamera er lukketiden. Hvis man tegner med lys, er det med at få den rigtige mængde lys. For lidt lys og fotoet bliver mørkt, for meget lys og fotoet bliver helt overblændet. </p>
                <p>Når man har en lang lukketid, kan billedet nå at bevæge sig, så derfor kan skal denne ikke bruges til bevægelige mål. Hvis man har en kort lukketid, kan man få ting der er i bevægelse til at stå helt stille på fotoet. Men det er på bekostning af lyset.</p>

                <h3>Dybdeskarphed</h3>
                <p>Med blænden indstiller man dybdeskarpheden. Et lille hul lukker mindre lys ind så den kan bruges med en høj lukketid. Med det lille hul følger også en høj skarphed i dybden, så der er fokus på både det der er fores i billede og det der er bagerst. Det er godt til landskabsbilleder.</p>
                <p>Med et stort hul i blænden lukkes meget lys ind, og derfor er den god og bruge med en lille lukketid. Her vil der til gengæl være en lille dybdeskarphed. Der er et eksemple på linkdinlearning-foto-gennemgang, hvor underviseren fotograferer en lille objekt, med en sort hul. Han kan derefter stille skarp på forskellige ting på objektet, og alt andet bliver sløret.</p>

                <h3>ISO</h3>
                <p>Den sidste parameter der er at skrue på er iso. Her kan man tvinge kameraet til at tage hurtigere billeder på bekostning af kvaliteten af fotoet.</p>

                <h3>Anvendelse</h3>
                <p>Den måde vi anvendte disse tre instillinger var at vælge en lukketid eller dybdeskarphed. Herefter valgte kameraet selv de sidste to parametre. Hvis man har god tid, f.eks. i et studie hvor man også har styr på lyset, vil man kunne vælge alle tre parrametre.</p>
