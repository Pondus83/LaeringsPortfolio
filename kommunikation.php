                <h3>AID(C)A(S)</h3>
                <p>Bruges til at fange og fastholde læserens opmærksomhed.</p>
                <ul>
                    <li>A = Opmærksomhed - Vi har fanget læserens opmærksomhed og kan bruge denne</li>
                    <li>I = Interesse - Det som læseren læser har også hans interesse</li>
                    <li>D = Lyst/Ønske - Vi giver læseren en lyst eller et ønske til at gøre noget</li>
                    <li>(C) = Troværdighed - Vi viser vi er troværdige så læseren tror på det han har læst</li>
                    <li>A = Handling - Vi giver læseren svaret på hans lyst eller ønske</li>
                    <li>(S) = Tilfredshed - Giver kunden mulighed for at udtrykke dennes tilfredshed for handlingen</li>
                </ul>
