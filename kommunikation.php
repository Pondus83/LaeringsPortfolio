                <p>Når man indsamler empiri er det vigtigt at have for øje hvad denne skal bruges til. Der findes to overordnede måder at samle empiri på. Enten den kvalitative eller den kvantitative indsamling af data. Disse metoder lægger sig op af paradigmerne. Ved en kvalitativ indsamling er vi interesseret i at vide hvorfor brugeren gør som han gør, ved en kvantitativ indsamling af emperi, er vi mere interesseret i at vide hvor mange der gør.</p>

