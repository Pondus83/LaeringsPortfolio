                <h2>Presentations teknik</h2>

                <h3>Beskrivelse</h3>
                <p>De første 20 sec er vigtige, så det gælder altså om at givet et godt første håndst intryk.<br>Derudover kommer kun 7% af indtrykket hos modtageren fra den verbale kommunikation. Den nonverbale fylder rigtig meget og skal skal tages alvorlig, når man arbejder med presentation hvad enten det er uddannelse, job eller salg.</p>
                
                <h3>Nonverbale</h3>
                <p>Kropssprog - Der skal være overensstemmelse mellem det du siger verbalt og det din krop siger nonverbal. Hvis dette ikke er tilfældet kan der let opstå usikkerhed hos modtageren og dette leder til dårlig overførsel af et budskab.<br>Man kan både snakke om aktiv og passivt kropssprog.</p>
                <p>Kropsholdning - En balanceret kropsholdning giver heller ikke støj på linjen, og det er med til at sikre, at modtageren kan fastholde koncentrationen på emnet.</p>
                <p>Af andet nonverbalt kropssprog kan nævnes:</p>
                <ul>
                    <li>Stemmeleje(betoning, vejrtrækning, taletempo)</li>
                    <li>Ansigtsudtryk</li>
                    <li>Øjnbevægelser</li>
                    <li>Gestus/Fagter</li>
                    <li>Håndtryk</li>
                    <li>Afstand til andre</li>
                    <li>Påklædning</li>
                </ul>
                

